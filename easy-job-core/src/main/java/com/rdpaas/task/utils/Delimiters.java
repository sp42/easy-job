package com.rdpaas.task.utils;

/**
 * 分隔符
 */
public interface Delimiters {
     String COMMA = ",";

     String DOT = ".";

     String LINE = "-";

     String COLON = ":";

     String BLANK = "\\s+";

     String COMMENT = "//";

     String NEW_LINE = "\n";

     String SLASH = "/";

     String ANGLE_BRACKETS = "<.*?>";

     String LEFT_BRACKET = "(";

     String RIGHT_BRACKET = ")";

}
